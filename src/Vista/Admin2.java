    public void cargartable(){
    
        //Cargar tabla de registro de clientes
        Conexion mysql = new Conexion();
       Connection cn=mysql.Conectar();
       
       try{
           //se manda llamar los atribusto de base de datos y el nombre de la tabla
           PreparedStatement pst = cn.prepareStatement("select IdUsuario,usuario, clave, Telefono, rol from usuarios");
           ResultSet rs =pst.executeQuery();
           
           jtbTablaUsuario =new JTable(model);
           jScrollPane1.setViewportView(jtbTablaUsuario);
           //agregar los campos que vana parecer en la tabla
           model.addColumn("Id");
//           model.addColumn("Nombre");
//           model.addColumn("Apellidos");
           model.addColumn("Usuario");
           model.addColumn("Contraseña");
           model.addColumn("Telefono");
           model.addColumn("Rol");
           
           ResultSetMetaData rsMd=rs.getMetaData();
           //declaramos un variable de tipo entero
           int cantidadColumna= rsMd.getColumnCount();
           while (rs.next()) {
               //cracion de matriz
                Object[] fila = new Object[cantidadColumna];
                 
                for (int i = 0; i < cantidadColumna; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            cn.close();
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null, "Error"+e, "En El Tabla", JOptionPane.ERROR_MESSAGE);
       }
}

