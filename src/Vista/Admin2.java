    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        
        int ID;
        Conexion mysql = new Conexion();
       Connection cn=mysql.Conectar();
       int profe=cbxRolUsers.getSelectedIndex();
    String rol=cbxRolUsers.getItemAt(profe);
    if(profe==0){
        rol="Ingeniero";
    }
    if(profe==1){
        rol="Ayudante1";
        }
    if(profe==2){
        rol="Ayudante2";
    }
    
        try{
            //
         PreparedStatement pst=cn.prepareStatement("select max(IdUsuario) from usuarios");
         ResultSet rs=pst.executeQuery();
         if(rs.next()){
                ID=rs.getInt(1)+ 1;
                //
         pst = cn.prepareStatement("insert into usuarios values(?,?,?,?,?)");
         pst.setInt(1,ID);
         pst.setString(2, txtUsuario.getText().trim());
         pst.setString(3, txtContra.getText().trim());
         pst.setString(4, txtTelefonoUsua.getText().trim());
         pst.setString(5, rol);
         pst.executeUpdate();
         }
         //Cargarlo en la tabla
           Object[] fila = new Object[6];
           fila[0]=txtIdUsua.getText();
//           fila[1]=txtNombreUsua.getText();
           fila[1]=txtUsuario.getText();
//           fila[3]=txtApellidoUsua.getText();
           fila[2]=txtContra.getText();
           fila[3]=txtTelefonoUsua.getText();
          
           
           model.addRow(fila);
         ///
         txtIdUsua.setText("");
//         txtNombreUsua.setText("");
//         txtApellidoUsua.setText("");
         txtUsuario.setText("");
         txtContra.setText("");
         txtTelefonoUsua.setText("");
         
         JOptionPane.showMessageDialog(null, "El registro se ingreso", "Ingreso", JOptionPane.INFORMATION_MESSAGE);
     }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Error"+e, "Ingreso", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

